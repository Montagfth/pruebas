    private void btnBuscar_LibrosPanelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscar_LibrosPanelActionPerformed
        //Accionamiento de busqueda de libros:
        //Viernes 24/01/2025
        try {
            //String de consulta en la base de datos:
            String BusquedaLibro = txtBusqueda_LibrosPanel.getText();
            //Validacion de busqueda:
            if (BusquedaLibro.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor, ingrese el nombre del libro a buscar.", "iBooking", JOptionPane.WARNING_MESSAGE);
                txtBusqueda_LibrosPanel.requestFocus();
                return;
            }
            //Comunicacion con los DAO:
            DAOLibros dao = new DAOLibrosImp();
            //Inicializacion de la tabla:
            DefaultTableModel model = (DefaultTableModel) tblBuscarLibro_LibrosPanel.getModel();
            //Valores de inicializacion para el conteo de filas:
            model.setRowCount(0);
            //Listado de columnas de la tabla:
            dao.Listar(BusquedaLibro).forEach((u) -> model.addRow(new Object[]{u.getId(), u.getTitle(), u.getfecha(), u.getAuthor(),
                u.getCategory(), u.getEdit(), u.getLang(), u.getPages(), u.getDescripcion(), u.getEjemplares(), u.getStock(), u.getAvailable()}));
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnBuscar_LibrosPanelActionPerformed

