    //Metodo de cargador/refrescado de libros del sistema:
    private void LoadLibros() {
        try {
            //Comunicacion con la base de datos:
            DAOLibros dao = new DAOLibrosImp();
            DefaultTableModel model = (DefaultTableModel) tblBuscarLibro_LibrosPanel.getModel();
            //Valores de inicializacion para el conteo de filas:
            model.setRowCount(0);
            //Listado de columnas de la tabla:
            dao.Listar("").forEach((u) -> model.addRow(new Object[]{u.getId(), u.getTitle(), u.getfecha(), u.getAuthor(),
                u.getCategory(), u.getEdit(), u.getLang(), u.getPages(), u.getDescripcion(), u.getEjemplares(), u.getStock(), u.getAvailable()}));
        } catch (Exception e) {
            //Captura del mensaje de error tras despliegue de accionamiento:
            System.out.println(e.getMessage());
        }
    }

