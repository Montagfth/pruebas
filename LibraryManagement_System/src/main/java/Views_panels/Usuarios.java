    private void btnBuscar_UsuariosPanelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscar_UsuariosPanelActionPerformed
        try {
            //Instanciacion del valor de la busqueda:
            String usuarioBusqueda = txtBusqueda_UsuariosPanel.getText();
            //Validadcion para el campo de texto de busqueda:
            //NOTA: Metod reutilizado de otro panel, adaptado a este entorno
            if (usuarioBusqueda.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor, debe ingresar el nombre de usuario a buscar.", "iBooking", JOptionPane.WARNING_MESSAGE);
                //Redireccion el cursor al cuadro de texto "txtFldNombre_RegistroUsuarioPanel"
                txtBusqueda_UsuariosPanel.requestFocus();
                return;
            }
            //CHECKPOINT: Domingo 12/01/2025 - 11:14, corregir las leyendas de los txtgaps [CORREGIDO]
            //Comunicacion de Interface con Implement:
            DAOUsuarios dao = new DAOUsuariosImpl();
            //Instanciacion de la tabla a usar:
            DefaultTableModel model = (DefaultTableModel) tblUsuarios_UsuariosPanel.getModel();
            //Prueba en consola de la consulta "SELECT ....":
            //Limpieza de los valores de busqueda post consulta:
            model.setRowCount(0);
            //dao.Listar().forEach((u) -> System.out.println(u.getName()));
            dao.Listar(usuarioBusqueda).forEach((u) -> model.addRow(new Object[]{u.getId(), u.getName(), u.getLast_name_p(), u.getLast_name_m(),
                u.getDomicilio(), u.getTel()}));
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnBuscar_UsuariosPanelActionPerformed


