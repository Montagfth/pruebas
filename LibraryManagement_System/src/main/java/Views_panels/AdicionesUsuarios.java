    private void btnRegistrar_RegistroUsuarioPanelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrar_RegistroUsuarioPanelActionPerformed
        //Instanciacion de los JTextFields correspondientes:
        String nombre = txtFldNombre_RegistroUsuarioPanel.getText();
        String apellidoPaterno = txtFldApellidoPaterno_RegistroUsuarioPanel.getText();
        String apellidoMaterno = txtFldApellidoMaterno_RegistroUsuarioPanel.getText();
        String domicilio = txtFldDomicilio_RegistroUsuarioPanel.getText();
        String telefono = txtFldTelefono_RegistroUsuarioPanel.getText();

        //Validadciones para los campos de entrada de texto:
        if (nombre.equals("Ingrese su nombre(s)") || apellidoPaterno.equals("Ingrese su apellido paterno")
                || apellidoMaterno.equals("Ingrese su apellido materno") || domicilio.equals("Ingrese la direccion de su domicilio")
                || telefono.equals("Ingrese su numero telefonico")) {
            JOptionPane.showMessageDialog(null, "Por favor, asegurese de que todos los campos estan completos, Gracias.", "iBooking", JOptionPane.WARNING_MESSAGE);
            //Redireccion el cursor al cuadro de texto "txtFldNombre_RegistroUsuarioPanel"
            txtFldNombre_RegistroUsuarioPanel.requestFocus();
            return;
        }

        //Adicion de la informacion capturada en los campos instanciados:
        //PROCESO: Si se trata de una edicion, se asigna automaticamente un nuevo usuario o lo que traiga en "usuarioEdition"
        //         caso contrario que si no se encuentra, se cree un nuvo obejto de tipo "Usuarios()".           
        Usuarios usuario = isEdition ? usuarioEdition : new Usuarios();
        usuario.setName(nombre);
        usuario.setLast_name_p(apellidoPaterno);
        usuario.setLast_name_m(apellidoMaterno);
        usuario.setDomicilio(domicilio);
        usuario.setTel(telefono);

        //Uso del "try{...} catch (Exception e) {...}" para localizar posibles errores de compilacion.
        try {
            DAOUsuarios dao = new DAOUsuariosImpl();

            //Validador de regristro y modificacion:
            if (!isEdition) {
                //Crea un nuevo objeto usuarioID():
                dao.Registrar(usuario);
            } else {
                //Captura un ID de usuario:
                dao.Modificar(usuario);
            }

            //Confirmador de exito de operacion:
            String successMessage = isEdition ? "modificado" : "registrado";
            //Anuncio pop-up de confirmacion de la operacion:
            //NOTA: Se aprecia que se utiliza el objeto de tipo String superior, para variar el mensaje segun sea el caso aplicativo.
            JOptionPane.showMessageDialog(null, "Usuario " + successMessage + " exitosamente :) ", "iBooking", JOptionPane.INFORMATION_MESSAGE);

            //Condicional usada para NO borrar automaticamente todos los campos de texto capturado previamente:
            if (!isEdition) {
                //Metodo de reestablecimiento de leyendas automaticamente tras transaccion exitosa:
                //Campo: Nombre
                txtFldNombre_RegistroUsuarioPanel.setText("Ingrese su nombre(s)");
                txtFldNombre_RegistroUsuarioPanel.setForeground(Color.gray);
                //Campo: Apellido Paterno
                txtFldApellidoPaterno_RegistroUsuarioPanel.setText("Ingrese su apellido paterno");
                txtFldApellidoPaterno_RegistroUsuarioPanel.setForeground(Color.gray);
                //Campo: Apellido Materno
                txtFldApellidoMaterno_RegistroUsuarioPanel.setText("Ingrese su apellido materno");
                txtFldApellidoMaterno_RegistroUsuarioPanel.setForeground(Color.gray);
                //Campo: Domicilio
                txtFldDomicilio_RegistroUsuarioPanel.setText("Ingrese la direccion de su domicilio");
                txtFldDomicilio_RegistroUsuarioPanel.setForeground(Color.gray);
                //Campo: Telefono
                txtFldTelefono_RegistroUsuarioPanel.setText("Ingrese su numero telefonico");
                txtFldTelefono_RegistroUsuarioPanel.setForeground(Color.gray);
                //CHECKPOINT: Sabado 11/01/2025 - 15:56
            }
        } catch (Exception e) {
            //Mensaje de error en base al caso propuesto:
            String errorMessage = isEdition ? "modificar" : "registrar";
            //NOTA: Se aprecia el uso del objeto de tipo String, el cual modifica el mensaje segun sea la operacion,
            //      se puede apreciar el mismo caso en el bloque superior de condicion exitosa.
            JOptionPane.showMessageDialog(null, "Ohps! Ocurrio un error al " + errorMessage + " el usuario. :(", "iBooking", JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrar_RegistroUsuarioPanelActionPerformed

